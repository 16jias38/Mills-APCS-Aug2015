public class FahrenheitToCelsius
{
 public static void main(String[] args)
 {
     double fahrenheit=75;
     System.out.println("Fahrenheit="+fahrenheit);
     System.out.println("Celsius="+(fahrenheit-32)*5/9);
    }
}
